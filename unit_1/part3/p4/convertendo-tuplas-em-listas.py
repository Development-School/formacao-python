
linhas = []
linhas.append("linha 1")
linhas.append("linha 2")
linhas.append("linha 3")
print(linhas)
# ['linha 1', 'linha 2', 'linha 3']

print(type(linhas))
# <class 'list'>

print("------------------------")

linhas_tuple = tuple(linhas)
print(linhas_tuple)
# ('linha 1', 'linha 2', 'linha 3')

print(type(linhas_tuple))
# <class 'tuple'>

print("------------------------")

linhas_list = list(linhas_tuple)
print(linhas_list)
['linha 1', 'linha 2', 'linha 3']

print(type(linhas_list))
# <class 'list'>













