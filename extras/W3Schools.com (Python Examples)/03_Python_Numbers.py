print("-----------------------------------------------")

# Verify the type of an object
x = 1
y = 2.8
z = 1j

print(type(x))
# <class 'int'>

print(type(y))
# <class 'float'>

print(type(z))
# <class 'complex'>

print("-----------------------------------------------")

# Create integers
x = 1
y = 35656222554887711
z = -3255522

print(type(x))
# <class 'int'>

print(type(y))
# <class 'int'>

print(type(z))
# <class 'int'>

print("-----------------------------------------------")

# Create floating point numbers
x = 1.10
y = 1.0
z = -35.59

print(type(x))
# <class 'float'>

print(type(y))
# <class 'float'>

print(type(z))
# <class 'float'>

print("-----------------------------------------------")

# Create scientific numbers with an "e" to indicate the power of 10
x = 35e3
y = 12E4
z = -87.7e100

print(type(x))
# <class 'float'>

print(type(y))
# <class 'float'>

print(type(z))
# <class 'float'>

print("-----------------------------------------------")

# Create complex numbers
x = 3+5j
y = 5j
z = -5j

print(type(x))
# <class 'complex'>

print(type(y))
# <class 'complex'>

print(type(z))
# <class 'complex'>

print("-----------------------------------------------")