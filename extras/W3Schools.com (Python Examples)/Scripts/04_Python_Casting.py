#-------------------------------------------------------------
#                     Python Casting
#-------------------------------------------------------------

print("-----------------------------------------------")

# Casting - Integers
x = int(1)
y = int(2.8)
z = int("3")
print(x)
print(y)
print(z)
# 1
# 2
# 3

print("-----------------------------------------------")

# Casting - Floats
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)
print(y)
print(z)
print(w)
# 1.0
# 2.8
# 3.0
# 4.2

print("-----------------------------------------------")

# Casting - Strings
x = str("s1")
y = str(2)
z = str(3.0)
print(x)
print(y)
print(z)
# s1
# 2
# 3.0

print("-----------------------------------------------")